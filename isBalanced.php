function isBalanced($str){
        $length = strlen($str);
        $leftBrecket = "";
        $rightBrecket = "";
        
        for ($i = 0; $i < $length; $i++ ){
            if($str[$i] == '(') {
                $leftBrecket++;
        } else if ($str[$i] == ')'){
            $rightBrecket++;
        }
        if ($leftBrecket < $rightBrecket ) {
            return false;
        }
    }
    if ($leftBrecket == $rightBrecket){
        return true;
    } else {
        return false;
    }
}
